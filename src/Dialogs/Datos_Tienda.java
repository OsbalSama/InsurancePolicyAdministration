    public void asign_datos() {
        try {
            String[] datos = new DatEmpresa().getDatEmpresa();
            txt_ntienda.setText(datos[1]);
            if ("SIN RFC".equals(datos[2])) {
                txt_rfc.setText(datos[2]);
                chk_rfc.setSelected(true);
                txt_rfc.setEditable(false);
            } else {
                txt_rfc.setText(datos[2]);
            }

            txt_sucursal.setText(datos[3]);
            txt_direccion.setText(datos[4]);
            if ("SIN TELEFONO".equals(datos[5])) {
                txt_tel.setText(datos[5]);
                chk_tel.setSelected(true);
                txt_tel.setEditable(false);
            } else {
                txt_tel.setText(datos[5]);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e, "¡ERROR!", JOptionPane.ERROR_MESSAGE);
        }
    }

